#Requires -Version 5.1

<#
.SYNOPSIS
    Run the graphify dry-run against rsl-siege-manager. Issue #397, Phase B.

.DESCRIPTION
    Builds a graphify knowledge graph for the project and writes artifacts to
    graphify-out/ at the project root. Graphify auto-detects the LLM backend
    from whichever API key env var is set (ANTHROPIC_API_KEY, OPENAI_API_KEY,
    GEMINI_API_KEY, MOONSHOT_API_KEY) - set one in your shell before invoking.
    This script does NOT inspect or echo the key.

    The script is idempotent - re-running rebuilds graphify-out/ from scratch.
    Nothing outside graphify-out/ and .graphifyignore is created or modified.

.PARAMETER ProjectRoot
    Path to the rsl-siege-manager repo root. Defaults to two levels above this script
    (so the script works whether invoked from the main checkout or a worktree
    that has the script committed).

.PARAMETER SkipIgnoreFile
    Don't auto-create .graphifyignore. Use if you've already authored one.

.PARAMETER NoDelay
    Skip the 5-second pre-build abort window (for CI/scripted invocations).

.EXAMPLE
    # In your own PowerShell, with ANTHROPIC_API_KEY already set:
    .\scripts\experiments\run-graphify-dry-run.ps1

.EXAMPLE
    # From elsewhere on disk:
    & 'C:\path\to\rsl-siege-manager\scripts\experiments\run-graphify-dry-run.ps1' `
        -ProjectRoot 'C:\path\to\rsl-siege-manager'

.NOTES
    Cost: one LLM API call per non-code, non-ignored file. Bounded by
    .graphifyignore (auto-created here, sane defaults for rsl-siege-manager).
    Estimate: $0.10-$2.00 depending on the doc/PDF/image count surviving
    the ignore filter. Watch terminal output; Ctrl+C is safe at any point.

    See docs/experiments/graphify-dry-run.md for the full evaluation plan.
#>

[CmdletBinding()]
param(
    [string] $ProjectRoot = (Join-Path $PSScriptRoot '..\..'),
    [switch] $SkipIgnoreFile,
    [switch] $NoDelay
)

$ErrorActionPreference = 'Stop'

Write-Host "Graphify Dry-Run for rsl-siege-manager (issue #397, Phase B)" -ForegroundColor Cyan
Write-Host ""

# 1) graphify CLI present? ----------------------------------------------------
$graphifyCmd = Get-Command graphify -ErrorAction SilentlyContinue
if (-not $graphifyCmd) {
    throw "graphify CLI not found on PATH. Install via: uv tool install graphifyy"
}
$graphifyVersion = (& graphify --version) -join ' '
Write-Host "graphify: $($graphifyCmd.Source) ($graphifyVersion)" -ForegroundColor Gray

# 2) Resolve project root -----------------------------------------------------
$ProjectRoot = (Resolve-Path $ProjectRoot).Path
if (-not (Test-Path (Join-Path $ProjectRoot '.git'))) {
    throw "ProjectRoot '$ProjectRoot' is not a git repo. Pass -ProjectRoot explicitly."
}
Write-Host "Project root: $ProjectRoot" -ForegroundColor Gray
Write-Host ""

Push-Location $ProjectRoot
try {
    # 3) Optional: create .graphifyignore -------------------------------------
    $ignorePath = Join-Path $ProjectRoot '.graphifyignore'
    if (-not $SkipIgnoreFile -and -not (Test-Path $ignorePath)) {
        Write-Host "Creating .graphifyignore (skip with -SkipIgnoreFile)..." -ForegroundColor Yellow
        @'
# Auto-generated by scripts/experiments/run-graphify-dry-run.ps1
# Bounds LLM-call cost during the graphify build.
# Patterns are .gitignore-style; ! negates.

# Vendored deps / build outputs (large, no signal)
.venv/
node_modules/
.worktrees/
dist/
build/
.next/
coverage/
htmlcov/

# Caches
__pycache__/
.pytest_cache/
.ruff_cache/
.mypy_cache/

# Generated / minified
*.generated.*
*.min.js
*.min.css

# Graphify own output
graphify-out/
'@ | Set-Content -Encoding utf8 -NoNewline $ignorePath
        Write-Host "  Wrote $ignorePath" -ForegroundColor Green
        Write-Host ""
    } elseif (Test-Path $ignorePath) {
        Write-Host ".graphifyignore exists; leaving as-is." -ForegroundColor Gray
        Write-Host ""
    }

    # 4) The cost step --------------------------------------------------------
    Write-Host "About to run: graphify extract ." -ForegroundColor Cyan
    Write-Host "  This will make one LLM API call per non-code, non-ignored file." -ForegroundColor Yellow
    Write-Host "  graphify auto-selects the backend from whichever API key env var is set." -ForegroundColor Yellow
    if (-not $NoDelay) {
        Write-Host "  Ctrl+C within 5s to abort." -ForegroundColor Yellow
        Start-Sleep -Seconds 5
    }
    Write-Host ""

    & graphify extract .
    $extractExit = $LASTEXITCODE
    if ($extractExit -ne 0) {
        throw "graphify extract exited with code $extractExit. See output above."
    }

    # 5) Post-run summary -----------------------------------------------------
    $outDir = Join-Path $ProjectRoot 'graphify-out'
    Write-Host ""
    Write-Host "Done. Artifacts in: $outDir" -ForegroundColor Green
    Write-Host ""

    $costFile = Join-Path $outDir 'cost.json'
    if (Test-Path $costFile) {
        Write-Host "Cost summary (cost.json):" -ForegroundColor Cyan
        Get-Content $costFile | Write-Host
        Write-Host ""
    }

    Write-Host "Next: inspect the artifacts." -ForegroundColor Cyan
    Write-Host "  code $(Join-Path $outDir 'GRAPH_REPORT.md')" -ForegroundColor Gray
    Write-Host "  Start-Process $(Join-Path $outDir 'graph.html')" -ForegroundColor Gray
    Write-Host '  graphify query "how does authentication flow through the app?"' -ForegroundColor Gray
    Write-Host ""
    Write-Host "Full plan + acid-test questions: docs/experiments/graphify-dry-run.md" -ForegroundColor Gray
}
finally {
    Pop-Location
}
